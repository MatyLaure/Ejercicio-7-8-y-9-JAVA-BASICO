package ArrayUnidimencional;

public class ArrayUnidimencional {
    public static void main(String[] args) {
        String paises[] = {
                "Argentina",
                "Francia",
                "Croacia",
                "Marruecos"
        };

        for (int i = 0; i < paises.length; i++) {
            System.out.println(i);
        }

    }
}
